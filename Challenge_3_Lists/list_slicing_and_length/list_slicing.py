# Welcome message to the user
print("welcome to the colors list")
# Defining a list of colors
colors = ['red', 'blue', 'green', 'yellow', 'purple']
# Printing the complete list of colors
print(colors)
# Informing the user that a portion of the list will be sliced
print("sliced the list for only the second and third colors:")
# The slicing [1:3] starts from index 1 and ends at index 3
print(colors[1:3])
