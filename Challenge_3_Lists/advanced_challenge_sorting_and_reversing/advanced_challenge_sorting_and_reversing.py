# Printing a welcome message for the program
print("welcome to sorting and reversing a list")
# Initializing a list of fruits
fruits = ['banana', 'apple', 'grape', 'kiwi', 'orange']
# Sorting the list of fruits in alphabetical order (ascending)
fruits.sort()
# Printing the sorted list of fruits
print("Sorted Fruits:")
print(fruits)
# Reversing the sorted list of fruits
fruits.reverse()
# Printing the reversed list of sorted fruits
print("Reversed Sorted Fruits:")
print(fruits)
